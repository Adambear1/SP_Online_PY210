#!/usr/bin/env python3

#Dictionary 1
dict_1 = {"name": "Chris", "city": "Seattle", "cake": "Chocolate"}

#Display the dict
print(dict_1)

#Delete the entry for cake, display the dict
dict_1.pop("cake")
print(dict_1)

#add fruit, display dict
dict_1["fruit"] = "Mango"
print(dict_1)

#display dict keys
print(dict_1.keys())

#display the dict values
print(dict_1.values())




